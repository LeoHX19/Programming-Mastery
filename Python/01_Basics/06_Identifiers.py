# Valid Identifiers

student_name = "Brad Pitt"
age = 22
course1 = "Python"
_marks = 95
totalMarks = 500

print("Student Name :", student_name)
print("Age          :", age)
print("Course       :", course1)
print("Marks        :", _marks)
print("Total Marks  :", totalMarks)

print("\nRules for Identifiers:")
print("1. Must start with a letter (A-Z or a-z) or an underscore (_).")
print("2. Cannot start with a number.")
print("3. Can contain letters, digits, and underscores.")
print("4. Cannot contain spaces or special characters.")
print("5. Cannot use Python keywords as identifiers.")
print("6. Python identifiers are case-sensitive.")