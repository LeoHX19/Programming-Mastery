# Assignment Operators
num = 10

print("Value:", num)

num += 5
print("After += :", num)

num -= 2
print("After -= :", num)

num *= 3
print("After *= :", num)

num /= 2
print("After /= :", num)

num = 10

num //= 3
print("After //= :", num)

num = 10

num %= 3
print("After %= :", num)

num = 2

num **= 3
print("After **= :", num)